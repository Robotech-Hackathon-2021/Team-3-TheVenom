package com.example.projecthackathon.Classes;

public class Student {
    String studName,
            studEmail,
            studRollNumber,
            studPassword,
            studPhoneNumber,
            studFatherName,
            studAddress,
            studYearAndBranch,
            studDOB,
            studGender;

    public String getStudName() {
        return studName;
    }

    public void setStudName(String studName) {
        this.studName = studName;
    }

    public String getStudEmail() {
        return studEmail;
    }

    public void setStudEmail(String studEmail) {
        this.studEmail = studEmail;
    }

    public String getStudRollNumber() {
        return studRollNumber;
    }

    public void setStudRollNumber(String studRollNumber) {
        this.studRollNumber = studRollNumber;
    }

    public String getStudPassword() {
        return studPassword;
    }

    public void setStudPassword(String studPassword) {
        this.studPassword = studPassword;
    }

    public String getStudPhoneNumber() {
        return studPhoneNumber;
    }

    public void setStudPhoneNumber(String studPhoneNumber) {
        this.studPhoneNumber = studPhoneNumber;
    }

    public String getStudFatherName() {
        return studFatherName;
    }

    public void setStudFatherName(String studFatherName) {
        this.studFatherName = studFatherName;
    }

    public String getStudAddress() {
        return studAddress;
    }

    public void setStudAddress(String studAddress) {
        this.studAddress = studAddress;
    }

    public String getStudYearAndBranch() {
        return studYearAndBranch;
    }

    public void setStudYearAndBranch(String studYearAndBranch) {
        this.studYearAndBranch = studYearAndBranch;
    }

    public String getStudDOB() {
        return studDOB;
    }

    public void setStudDOB(String studDOB) {
        this.studDOB = studDOB;
    }

    public String getStudGender() {
        return studGender;
    }

    public void setStudGender(String studGender) {
        this.studGender = studGender;
    }
}
